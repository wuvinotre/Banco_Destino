    private void jBconfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconfirmaActionPerformed
        // TODO add your handling code here:
        if (jCvalor.isEnabled() && jToutroValor.getText().isEmpty()) {
            double temp;
            temp = Double.parseDouble(jCvalor.getSelectedItem().toString());
            if (saque(temp)) {
                JOptionPane.showMessageDialog(rootPane, "Saque realizado com sucesso");
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saque não foi realizado");
            }
        } else {
            double temp;
            temp = Double.parseDouble(jToutroValor.getText());
            if (saque(temp)) {
                JOptionPane.showMessageDialog(rootPane, "Saque realizado com sucesso");
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saque não foi realizado");
            }
        }
    }//GEN-LAST:event_jBconfirmaActionPerformed

