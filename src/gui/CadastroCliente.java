    private void goCadastro(){
        Cliente cli = new Cliente();
        Endereco end = new Endereco();
        GetTenantId t = new GetTenantId();        
        Conta c = new Conta();
        String retorno;
        if(ValidaDados() && ValidaCpf()){
            ClienteDAO cliDAO = new ClienteDAO();
                        
            cli.setNome(jTnome.getText());
            cli.setSobrenome(jTsobrenome.getText());
            cli.setCpf(jTcpf.getText());
            System.out.println(jTtelefone.getText());
            int tmp = Integer.parseInt(jTtelefone.getText());
            cli.setNr_telefone(tmp);
            cli.setSenha(jTsenha.getText());
            retorno = "Cadastro cliente:\n"+cliDAO.inserir(cli)+"\n";
            
            EnderecoDAO endDAO = new EnderecoDAO();
            end.setNome_rua(jTrua.getText());
            end.setNumero(Integer.parseInt(jTnumero.getText()));
            end.setCidade(jCcidade.getSelectedItem().toString());
            end.setUf(jCuf.getSelectedItem().toString());
            int w = t.GetTenantId(jTcpf.getText());
            end.setTenant_id(w);
            retorno = retorno+"Cadastro Endere√ßo:\n"+endDAO.inserir(end)+"\n";
            
            
            ContaDAO cDAO = new ContaDAO();
            ConvInt conv = new ConvInt();
            c.setAgencia(conv.convInt(jTagencia.getText()));            
            c.setLimiteDeSaque(conv.convFloat(jTlimite.getText()));
            c.setN_conta(conv.convInt(jTnumeroConta.getText()));
            c.setSaldo(conv.convFloat(jTrendimento.getText()));
            switch(jCconta.getSelectedItem().toString()){
                case "Corrente" : c.setTipo(1);
                    break;
                case "Poupanca" : c.setTipo(2);
                     break;
                default : c.setTipo(0);                
            }
            c.setTenant_id(t.GetTenantId(jTcpf.getText()));
            retorno = retorno+"Cadastro conta:\n"+cDAO.inserir(c);
            JOptionPane.showMessageDialog(rootPane, retorno);
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro Preenchimento dos dados, favor verificar.");
        }
        
    }

