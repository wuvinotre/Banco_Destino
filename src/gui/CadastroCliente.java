    private boolean ValidaCpf(){
        if(jTcpf.getText().length() == 11 ){
            return true;
        }else{
            return false;
        }
    }
    
    private boolean ValidaDados(){
        if(jTnome.getText().length() > 0 && jTsobrenome.getText().length() > 0 && jTtelefone.getText().length() > 0 && jTrendimento.getText().length() > 0 && jTnumero.getText().length() > 0 && jTrua.getText().length() > 0){
            return true;
        }else{
            return false;
        }
    }
    
